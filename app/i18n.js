export function getEventDayText(lang, days) {
    const labels = i18n[lang] || i18n.ar;
    return days === 0 ? labels.eventToday : labels.eventRemaining(days);
}

export function getTodayInfo({ dayName, gregDay, gregMonth, hijriDay, hijriMonth }) {
    return `اليوم ${dayName}، ${gregDay} ${gregMonth} م | ${hijriDay} ${hijriMonth} هـ`;
}

export function buildEventsShareText(lang, events, url) {
    const labels = i18n[lang] || i18n.ar;
    const lines = [labels.shareEventsTitle, ''];

    events.forEach((event) => {
        lines.push(`${event.name}: ${getEventDayText(lang, event.days)}`);
    });

    lines.push('', url);
    return lines.join('\n');
}

export function getSeasonKey(month) {
    if (month >= 3 && month <= 5) return 'spring';
    if (month >= 6 && month <= 8) return 'summer';
    if (month >= 9 && month <= 11) return 'autumn';
    return 'winter';
}

export function buildDateStory(lang, { dayName, seasonKey, isLeap, title, rawResultText, url }) {
    const labels = i18n[lang] || i18n.ar;
    const season = labels.seasons[seasonKey];
    const leapText = isLeap ? labels.leapYes : labels.leapNo;

    if (lang === 'en') {
        return {
            info: `Falls on ${dayName} • Season: ${season} • Leap year: ${leapText}`,
            shareText: `${labels.pageTitle}\n\n${title}\nResult: ${rawResultText}\n\nThis date falls on ${dayName} during ${season}.\n\n${url}`,
        };
    }

    return {
        info: `يوافق يوم ${dayName} • فصل ${season} • سنة كبيسة: ${leapText}`,
        shareText: `${labels.pageTitle}\n\n${title}\nالنتيجة: ${rawResultText}\n\nهل تعلم؟ هذا التاريخ يوافق يوم ${dayName} وكان في فصل ${season}.\n\nجرّب الأداة:\n${url}`,
    };
}
